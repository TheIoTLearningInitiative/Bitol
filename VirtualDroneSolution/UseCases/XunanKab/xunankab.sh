#!/usr/bin/env bash

set -x

# =============================================================================
# Variables
# =============================================================================

NETWORK_NAME="xunankab_network"

# =============================================================================
# Main
# =============================================================================

docker network ls --filter name=${NETWORK_NAME} | grep ${NETWORK_NAME}
if [ $? -ne 0 ]; then
    docker network create --subnet=172.20.0.0/16 ${NETWORK_NAME}
fi
